const color = prompt("Enter your fav color: ");

switch (color) {
    case "red":
        alert("Red is pretty cool");
        break;

    case "blue":
        alert("Blue is the sky");
        break;

    case "orange":
        alert("Orange is my fav");
        break;

    default:
        alert("I do NOT like the color");
        break;
}

